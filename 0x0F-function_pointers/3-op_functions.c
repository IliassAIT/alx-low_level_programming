#include "3-calc.h"
/**
 * op_add - function that operates the addition of 2 ints
 * @a: integer to be operated.
 * @b: integer 2 to be operated.
 * Return: an (int) the result of the addition.
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_mul - function that operates the multiplicationn of 2 ints
 * @a: integer to be operated.
 * @b: integer 2 to be operated.
 * Return: an (int) the result of the multiplication.
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_sub - function that operates the substration of 2 ints
 * @a: integer to be operated.
 * @b: integer 2 to be operated.
 * Return: an (int) the result of the substration.
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mod - function that operates the division of 2 ints to give the
 * remainder of the division
 * @a: integer to be operated.
 * @b: integer 2 to be operated.
 * Return: an (int) which is the mod.
 */
int op_mod(int a, int b)
{
return (a % b);
}
/**
 * op_div - function that operates the divison of 2 ints
 * @a: integer to be operated.
 * @b: integer 2 to be operated.
 * Return: an (int) result of the division.
 */
int op_div(int a, int b)
{
return (a / b);
}
